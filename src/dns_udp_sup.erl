-module(dns_udp_sup).

-behaviour(supervisor).
%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([
		 start_link/1
		 ]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([
	 init/1,
	 terminate/2,
	 start_server/1
        ]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
%% -define(SERVER, ?MODULE).

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================


%%-----------------------------------------------------------------
%% Func: start_link
%% Description: Starts the UDP port server supervisor
%%-----------------------------------------------------------------
start_link(Options) ->
	supervisor:start_link(?MODULE, [Options]).


%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([Options]) ->
	SupFlags = {one_for_one, 500, 100},
    ChildSpec = [
				 {dns_udp_server, 
				  {?MODULE, start_server, [Options]},
				  permanent,
				  10000,
				  worker,
				  []}
				 ],
	{ok, {SupFlags, ChildSpec}}.
		

%%-----------------------------------------------------------------
%% Func: terminate/1
%% Description: Termination function for the supervisor
%%-----------------------------------------------------------------
terminate(_Reason, _State) ->
    ok.


%% ====================================================================
%% Internal functions
%% ====================================================================
%%-----------------------------------------------------------------
%% Func: start_server/1
%% Description: Function which the supervisor calls to start a child
%%-----------------------------------------------------------------
start_server(Args)->
	dns_udp_server:start_link(Args).
